    def get_rope_index(
        self,
        input_ids: Optional[torch.LongTensor] = None,
        image_grid_thw: Optional[torch.LongTensor] = None,
        video_grid_thw: Optional[torch.LongTensor] = None,
        second_per_grid_ts: Optional[torch.Tensor] = None,
        attention_mask: Optional[torch.Tensor] = None,
    ) -> Tuple[torch.Tensor, torch.Tensor]:
        spatial_merge_size = self.config.vision_config.spatial_merge_size
        image_token_id = self.config.image_token_id
        video_token_id = self.config.video_token_id
        vision_start_token_id = self.config.vision_start_token_id
        mrope_position_deltas = []
        if input_ids is not None and (image_grid_thw is not None or video_grid_thw is not None):
            total_input_ids = input_ids
            if attention_mask is None:
                attention_mask = torch.ones_like(total_input_ids)
            position_ids = torch.ones(
                3,
                input_ids.shape[0],
                input_ids.shape[1],
                dtype=input_ids.dtype,
                device=input_ids.device,
            )
            image_index, video_index = 0, 0
            attention_mask = attention_mask.to(total_input_ids.device)
            for i, input_ids in enumerate(total_input_ids):
                input_ids = input_ids[attention_mask[i] == 1]
                image_nums, video_nums = 0, 0
                vision_start_indices = torch.argwhere(input_ids == vision_start_token_id).squeeze(1)
                vision_tokens = input_ids[vision_start_indices + 1]
                image_nums = (vision_tokens == image_token_id).sum()
                video_nums = (vision_tokens == video_token_id).sum()
                input_tokens = input_ids.tolist()
                llm_pos_ids_list: list = []
                st = 0
                remain_images, remain_videos = image_nums, video_nums
                for _ in range(image_nums + video_nums):
                    if image_token_id in input_tokens and remain_images > 0:
                        ed_image = input_tokens.index(image_token_id, st)
                    else:
                        ed_image = len(input_tokens) + 1
                    if video_token_id in input_tokens and remain_videos > 0:
                        ed_video = input_tokens.index(video_token_id, st)
                    else:
                        ed_video = len(input_tokens) + 1
                    if ed_image < ed_video:
                        t, h, w = (
                            image_grid_thw[image_index][0],
                            image_grid_thw[image_index][1],
                            image_grid_thw[image_index][2],
                        )
                        second_per_grid_t = 0
                        image_index += 1
                        remain_images -= 1
                        ed = ed_image
                    else:
                        t, h, w = (
                            video_grid_thw[video_index][0],
                            video_grid_thw[video_index][1],
                            video_grid_thw[video_index][2],
                        )
                        second_per_grid_t = second_per_grid_ts[video_index] if second_per_grid_ts is not None else 1.0
                        video_index += 1
                        remain_videos -= 1
                        ed = ed_video
                    llm_grid_t, llm_grid_h, llm_grid_w = (
                        t.item(),
                        h.item() // spatial_merge_size,
                        w.item() // spatial_merge_size,
                    )
                    text_len = ed - st
                    st_idx = llm_pos_ids_list[-1].max() + 1 if len(llm_pos_ids_list) > 0 else 0
                    llm_pos_ids_list.append(torch.arange(text_len).view(1, -1).expand(3, -1) + st_idx)
                    range_tensor = torch.arange(llm_grid_t).view(-1, 1)
                    expanded_range = range_tensor.expand(-1, llm_grid_h * llm_grid_w)
                    time_tensor = expanded_range * second_per_grid_t * self.config.vision_config.tokens_per_second
                    t_index = time_tensor.long().flatten()
                    h_index = torch.arange(llm_grid_h).view(1, -1, 1).expand(llm_grid_t, -1, llm_grid_w).flatten()
                    w_index = torch.arange(llm_grid_w).view(1, 1, -1).expand(llm_grid_t, llm_grid_h, -1).flatten()
                    llm_pos_ids_list.append(torch.stack([t_index, h_index, w_index]) + text_len + st_idx)
                    st = ed + llm_grid_t * llm_grid_h * llm_grid_w
                if st < len(input_tokens):
                    st_idx = llm_pos_ids_list[-1].max() + 1 if len(llm_pos_ids_list) > 0 else 0
                    text_len = len(input_tokens) - st
                    llm_pos_ids_list.append(torch.arange(text_len).view(1, -1).expand(3, -1) + st_idx)
                llm_positions = torch.cat(llm_pos_ids_list, dim=1).reshape(3, -1)
                position_ids[..., i, attention_mask[i] == 1] = llm_positions.to(position_ids.device)
                mrope_position_deltas.append(llm_positions.max() + 1 - len(total_input_ids[i]))
            mrope_position_deltas = torch.tensor(mrope_position_deltas, device=input_ids.device).unsqueeze(1)
            return position_ids, mrope_position_deltas
        else:
            if attention_mask is not None:
                position_ids = attention_mask.long().cumsum(-1) - 1
                position_ids.masked_fill_(attention_mask == 0, 1)
                position_ids = position_ids.unsqueeze(0).expand(3, -1, -1).to(attention_mask.device)
                max_position_ids = position_ids.max(0, keepdim=False)[0].max(-1, keepdim=True)[0]
                mrope_position_deltas = max_position_ids + 1 - attention_mask.shape[-1]
            else:
                position_ids = torch.arange(input_ids.shape[1], device=input_ids.device).view(1, 1, -1).expand(3, input_ids.shape[0], -1)
                mrope_position_deltas = torch.zeros([input_ids.shape[0], 1], device=input_ids.device, dtype=input_ids.dtype)
            return position_ids, mrope_position_deltas

    def forward(
        self,
        input_ids: torch.LongTensor = None,
        attention_mask: Optional[torch.Tensor] = None,
        position_ids: Optional[torch.LongTensor] = None,
        past_key_values: Optional[List[torch.FloatTensor]] = None,
        inputs_embeds: Optional[torch.FloatTensor] = None,
        labels: Optional[torch.LongTensor] = None,
        use_cache: Optional[bool] = None,
        output_attentions: Optional[bool] = None,
        output_hidden_states: Optional[bool] = None,
        return_dict: Optional[bool] = None,
        pixel_values: Optional[torch.Tensor] = None,
        pixel_values_videos: Optional[torch.FloatTensor] = None,
        image_grid_thw: Optional[torch.LongTensor] = None,
        video_grid_thw: Optional[torch.LongTensor] = None,
        rope_deltas: Optional[torch.LongTensor] = None,
        cache_position: Optional[torch.LongTensor] = None,
        second_per_grid_ts: Optional[torch.Tensor] = None,
        images_vggt: Optional[List[torch.Tensor]] = None,
        boxes: Optional[List[torch.Tensor]] = None,
        tag: str = None,
        unlabeled_images: Optional[torch.Tensor] = None, 
        topo_labels: Optional[torch.LongTensor] = None, 
        **kwargs,
    ) -> Union[Tuple, Qwen2_5_VLCausalLMOutputWithPast]:
        
        output_attentions = output_attentions if output_attentions is not None else self.config.output_attentions
        output_hidden_states = output_hidden_states if output_hidden_states is not None else self.config.output_hidden_states
        return_dict = return_dict if return_dict is not None else self.config.use_return_dict

        if inputs_embeds is None:
            self.vggt.eval()
            inputs_embeds = self.model.embed_tokens(input_ids)
            
            if self.training:
                inputs_embeds.requires_grad_(True)
            
        dummy_loss = 0.0 * inputs_embeds.sum() if self.training else 0.0
        
        # =========================================================================
        # 👑 终极分布对齐：确保所有卡都使用全局最大的序列和模块调用顺序 👑
        # =========================================================================
        local_vggt_calls = 0
        if pixel_values is not None and images_vggt is not None:
            for i in range(inputs_embeds.shape[0]):
                if i < len(images_vggt) and images_vggt[i] is not None:
                    local_vggt_calls += images_vggt[i].shape[0]
                    
        global_max_vggt_calls = local_vggt_calls
        if self.training and torch.distributed.is_initialized():
            max_calls_tensor = torch.tensor([local_vggt_calls], dtype=torch.long, device=inputs_embeds.device)
            torch.distributed.all_reduce(max_calls_tensor, op=torch.distributed.ReduceOp.MAX)
            global_max_vggt_calls = max_calls_tensor.item()
            
        sms = self.config.vision_config.spatial_merge_size
        ps = self.config.vision_config.patch_size
        tps = getattr(self.config.vision_config, "temporal_patch_size", 2)
        ic = getattr(self.config.vision_config, "in_channels", 3)
        
        dummy_seq_len = 1 * sms * sms
        dummy_grid = torch.tensor([[1, sms, sms]], dtype=torch.long, device=inputs_embeds.device)
        dummy_pixel_values = inputs_embeds.new_zeros((dummy_seq_len, ic * tps * ps * ps))

        # ====================================================================================
        # 第一阶段：严格的序列化调用 (VGGT -> Merger -> Visual_Img -> Visual_Vid -> Topo -> Classifier)
        # ====================================================================================
        
        batched_features = []
        actual_calls_made = 0
        
        # 1. 统一执行 VGGT
        if pixel_values is not None:
            batch_size = inputs_embeds.shape[0]
            for i in range(batch_size):
                if images_vggt is not None and len(images_vggt) > i and images_vggt[i] is not None and images_vggt[i].shape[0] > 0:
                    n_image = 1
                    height, width = images_vggt[i].shape[-2:]
                    merge_size = self.config.vision_config.spatial_merge_size
                    h_grid, w_grid = height // ps, width // ps
                    h_grid_after_merge, w_grid_after_merge = h_grid // merge_size, w_grid // merge_size
                    dtype = torch.bfloat16 if torch.cuda.get_device_capability()[0] >= 8 else torch.float16
                    with torch.no_grad():
                        with torch.amp.autocast('cuda', dtype=dtype):
                            if not self.config.reference_frame=="first":
                                images_vggt[i] = torch.flip(images_vggt[i], dims=(0,))
                            self.past_key_values_vggt = [None] * self.vggt.aggregator.depth
                            for k, frame in enumerate(images_vggt[i]):
                                images = frame.unsqueeze(0).unsqueeze(0) 
                                aggregator_output = self.vggt.aggregator(images, past_key_values=self.past_key_values_vggt, use_cache=True, past_frame_idx=k)
                                actual_calls_made += 1
                                if isinstance(aggregator_output, tuple) and len(aggregator_output) == 3:
                                    aggregated_tokens, patch_start_idx, self.past_key_values_vggt = aggregator_output
                                else:
                                    aggregated_tokens, patch_start_idx = aggregator_output
                    features = aggregated_tokens[-2][0,:, patch_start_idx:]
                    if not self.config.reference_frame=="first":
                        features = torch.flip(features, dims=(0,))
                    features = features.view(n_image, h_grid, w_grid, -1)
                    features = features[:, :h_grid_after_merge * merge_size, :w_grid_after_merge * merge_size, :].contiguous()
                    features = features.view(n_image, h_grid_after_merge, merge_size, w_grid_after_merge, merge_size, -1)
                    features = features.permute(0, 1, 3, 2, 4, 5).contiguous().to(self.visual.dtype)
                    batched_features.append(features)
                    
        # 1.5 补齐欠缺的 VGGT 调用
        if self.training:
            needed = global_max_vggt_calls - actual_calls_made
            if needed > 0:
                d_v_img = inputs_embeds.new_zeros((1, 1, 3, 224, 224))
                for _ in range(needed):
                    v_out = self.vggt.aggregator(d_v_img, use_cache=False)
                    def _sum_tensors(obj):
                        if isinstance(obj, torch.Tensor): return obj.sum() if obj.dtype.is_floating_point else 0.0
                        elif isinstance(obj, (list, tuple)): return sum(_sum_tensors(x) for x in obj)
                        return 0.0
                    dummy_loss += _sum_tensors(v_out) * 0.0

        # 2. 统一执行 Merger
        if len(batched_features) > 0:
            features_cat = torch.cat(batched_features, dim=0)
            image_embeds_3d = self.merger(features_cat)
        else:
            image_embeds_3d = torch.empty((0, self.config.hidden_size), device=inputs_embeds.device, dtype=self.visual.dtype)
            if self.training:
                spatial_sq = self.merger.input_dim // 2048
                d_m_input = inputs_embeds.new_zeros((1, spatial_sq, 2048))
                dummy_loss += self.merger(d_m_input).sum() * 0.0

        # 3. 统一执行 Visual (Image)
        if pixel_values is not None:
            pixel_values = pixel_values.type(self.visual.dtype)
            image_embeds = self.visual(pixel_values, grid_thw=image_grid_thw)
            
            if image_embeds_3d.shape[0] > 0:
                if image_embeds_3d.shape[0] != image_embeds.shape[0]:
                    target_N = image_embeds.shape[0]
                    aligned_3d = image_embeds_3d.unsqueeze(0).transpose(1, 2).float()
                    aligned_3d = F.interpolate(aligned_3d, size=target_N, mode='linear', align_corners=False)
                    image_embeds_3d = aligned_3d.transpose(1, 2).squeeze(0).to(image_embeds.dtype)
                image_embeds = image_embeds + self.lam * image_embeds_3d
            
            n_image_tokens = (input_ids == self.config.image_token_id).sum().item()
            n_image_features = image_embeds.shape[0]
            mask = input_ids == self.config.image_token_id
            image_embeds = image_embeds.to(inputs_embeds.device, inputs_embeds.dtype)
            if n_image_tokens != n_image_features:
                if n_image_tokens > n_image_features:
                    true_indices = mask.nonzero(as_tuple=True)
                    valid_indices = (true_indices[0][:n_image_features], true_indices[1][:n_image_features])
                    new_mask = torch.zeros_like(mask); new_mask[valid_indices] = True
                    new_image_mask = new_mask.unsqueeze(-1).expand_as(inputs_embeds).to(inputs_embeds.device)
                    inputs_embeds = inputs_embeds.masked_scatter(new_image_mask, image_embeds)
                else:
                    image_mask = mask.unsqueeze(-1).expand_as(inputs_embeds).to(inputs_embeds.device)
                    inputs_embeds = inputs_embeds.masked_scatter(image_mask, image_embeds[:n_image_tokens])
            else:
                image_mask = mask.unsqueeze(-1).expand_as(inputs_embeds).to(inputs_embeds.device)
                inputs_embeds = inputs_embeds.masked_scatter(image_mask, image_embeds)
        else:
            if self.training:
                dummy_loss += self.visual(dummy_pixel_values, grid_thw=dummy_grid).sum() * 0.0

        # 4. 统一执行 Visual (Video)
        if pixel_values_videos is not None:
            pixel_values_videos = pixel_values_videos.type(self.visual.dtype)
            video_embeds = self.visual(pixel_values_videos, grid_thw=video_grid_thw)
            n_video_tokens = (input_ids == self.config.video_token_id).sum().item()
            video_embeds = video_embeds.to(inputs_embeds.device, inputs_embeds.dtype)
            mask_v = input_ids == self.config.video_token_id
            if n_video_tokens != video_embeds.shape[0]:
                if n_video_tokens > video_embeds.shape[0]:
                    true_idx = mask_v.nonzero(as_tuple=True)
                    v_idx = (true_idx[0][:video_embeds.shape[0]], true_idx[1][:video_embeds.shape[0]])
                    new_m_v = torch.zeros_like(mask_v); new_m_v[v_idx] = True
                    new_v_mask = new_m_v.unsqueeze(-1).expand_as(inputs_embeds).to(inputs_embeds.device)
                    inputs_embeds = inputs_embeds.masked_scatter(new_v_mask, video_embeds)
                else:
                    v_mask = mask_v.unsqueeze(-1).expand_as(inputs_embeds).to(inputs_embeds.device)
                    inputs_embeds = inputs_embeds.masked_scatter(v_mask, video_embeds[:n_video_tokens])
            else:
                v_mask = mask_v.unsqueeze(-1).expand_as(inputs_embeds).to(inputs_embeds.device)
                inputs_embeds = inputs_embeds.masked_scatter(v_mask, video_embeds)
        else:
            if self.training:
                dummy_loss += self.visual(dummy_pixel_values, grid_thw=dummy_grid).sum() * 0.0

        if attention_mask is not None:
            attention_mask = attention_mask.to(inputs_embeds.device)

        if position_ids is None and (attention_mask is None or attention_mask.ndim == 2):
            if (cache_position is not None and cache_position[0] == 0) or self.rope_deltas is None or (past_key_values is None or past_key_values.get_seq_length() == 0):
                position_ids, rope_deltas = self.get_rope_index(input_ids, image_grid_thw, video_grid_thw, second_per_grid_ts, attention_mask)
                self.rope_deltas = rope_deltas
            else:
                batch_size, seq_length, _ = inputs_embeds.shape
                delta = (cache_position[0] + self.rope_deltas).to(inputs_embeds.device) if cache_position is not None else 0
                position_ids = torch.arange(seq_length, device=inputs_embeds.device).view(1, -1).expand(batch_size, -1)
                if cache_position is not None: delta = delta.repeat_interleave(batch_size // delta.shape[0], dim=0)
                position_ids = position_ids.add(delta).unsqueeze(0).expand(3, -1, -1)

        outputs = self.model(input_ids=None, position_ids=position_ids, attention_mask=attention_mask, past_key_values=past_key_values, inputs_embeds=inputs_embeds, use_cache=use_cache, output_attentions=output_attentions, output_hidden_states=output_hidden_states, return_dict=return_dict, cache_position=cache_position)

        hidden_states = outputs[0]
        logits = self.lm_head(hidden_states)
        loss = None
        
        # 5. 统一执行 Topo Loss 
        topo_loss_val = 0.0
        has_unlabeled = unlabeled_images is not None and topo_labels is not None and unlabeled_images.shape[0] > 0

        if has_unlabeled:
            try:
                B, L, C = unlabeled_images.shape
                flat_un = unlabeled_images.view(-1, C).to(self.visual.dtype)
                gs = int(math.sqrt(L))
                un_grid = torch.tensor([[1, gs, gs]], dtype=torch.long, device=unlabeled_images.device).repeat(B, 1)
                un_feat = self.visual(flat_un, grid_thw=un_grid)
                pooled = un_feat.view(B, -1, un_feat.shape[-1]).mean(dim=1)
                topo_loss_val = CrossEntropyLoss()(self.topo_head(pooled), topo_labels.view(-1))
            except Exception as e:
                print(f"\n[Warning] Topo Loss Skip: {e}")
                has_unlabeled = False 

        if not has_unlabeled and self.training:
            dummy_v = self.visual(dummy_pixel_values, grid_thw=dummy_grid)
            dummy_loss += self.topo_head(dummy_v.mean(0, keepdim=True)).sum() * 0.0
            
        # 6. 统一执行 Classifier
        if labels is not None:
            logits = logits.float()
            shift_logits = logits[..., :-1, :].contiguous()
            shift_labels = labels[..., 1:].contiguous()
            loss_fct = CrossEntropyLoss()
            shift_logits = shift_logits.view(-1, self.config.vocab_size)
            shift_labels = shift_labels.view(-1).to(shift_logits.device)
            if hasattr(self.config, "add_ground_classifier") and self.config.add_ground_classifier:
                shift_labels = shift_labels.masked_fill(shift_labels == self.config.box_3d_token_ids[1], -100)
            loss = loss_fct(shift_logits, shift_labels)
            
            if boxes is not None and len(boxes) > 0:
                select_hidden_states = hidden_states[input_ids==self.config.box_3d_token_ids[0]].view(-1, hidden_states.shape[-1])
                if select_hidden_states.shape[0] > 0:
                    loss += F.mse_loss(self.classifier(select_hidden_states), boxes)
                elif hasattr(self, "classifier") and self.training:
                    dummy_loss += self.classifier(hidden_states[0:1, 0:1, :].reshape(1, -1) * 0.0).sum() * 0.0
            else:
                if hasattr(self, "classifier") and self.training:
                    dummy_loss += self.classifier(hidden_states[0:1, 0:1, :].reshape(1, -1) * 0.0).sum() * 0.0
        else:
            if hasattr(self, "classifier") and self.training:
                dummy_loss += self.classifier(hidden_states[0:1, 0:1, :].reshape(1, -1) * 0.0).sum() * 0.0
        
        # 统一计算最终 Loss
        if loss is None:
            loss = self.gamma * topo_loss_val + dummy_loss
        else:
            loss = loss + self.gamma * topo_loss_val + dummy_loss

        if not return_dict:
            output = (logits,) + outputs[1:]
            return (loss,) + output if loss is not None else output

        return Qwen2_5_VLCausalLMOutputWithPast(loss=loss, logits=logits, past_key_values=outputs.past_key_values, hidden_states=outputs.hidden_states, attentions=outputs.attentions, rope_deltas=self.rope_deltas)

    def prepare_inputs_for_generation(self, input_ids, past_key_values=None, attention_mask=None, inputs_embeds=None, cache_position=None, position_ids=None, use_cache=True, pixel_values=None, pixel_values_videos=None, image_grid_thw=None, video_grid_thw=None, second_per_grid_ts=None, **kwargs):
        model_inputs = super().prepare_inputs_for_generation(input_ids, past_key_values=past_key_values, attention_mask=attention_mask, inputs_embeds=inputs_embeds, cache_position=cache_position, position_ids=position_ids, pixel_values=pixel_values, pixel_values_videos=pixel_values_videos, image_grid_thw=image_grid_thw, video_grid_thw=video_grid_thw, second_per_grid_ts=second_per_grid_ts, use_cache=use_cache, **kwargs)
        model_inputs["position_ids"] = None
        if cache_position[0] != 0:
            model_inputs["pixel_values"] = None
            model_inputs["pixel_values_videos"] = None
        return model_inputs

    def _get_image_nums_and_video_nums(self, input_ids: Optional[torch.LongTensor]) -> Tuple[torch.Tensor, torch.Tensor]:
        v_start = input_ids == self.config.vision_start_token_id
        v_first = torch.roll(v_start, shifts=1, dims=1)
        i_nums = torch.sum(v_first & (input_ids == self.config.image_token_id), dim=1)
        v_nums = torch.sum(v_first & (input_ids == self.config.video_token_id), dim=1)
        return i_nums, v_nums

    def _expand_inputs_for_generation(self, expand_size: int = 1, is_encoder_decoder: bool = False, input_ids: Optional[torch.LongTensor] = None, **model_kwargs) -> Tuple[torch.LongTensor, Dict[str, Any]]:
        if expand_size == 1: return input_ids, model_kwargs
        v_keys = ["pixel_values", "image_grid_thw", "pixel_values_videos", "video_grid_thw", "second_per_grid_ts"]
        i_nums, v_nums = self._get_image_nums_and_video_nums(input_ids)
        for key in v_keys:
            if key in model_kwargs and model_kwargs[key] is not None:
                if key in ["pixel_values", "pixel_values_videos"]:
                    grid = model_kwargs["image_grid_thw"] if key == "pixel_values" else model_kwargs["video_grid_thw"]
                    nums = i_nums if key == "pixel_values" else v_nums
                    samples = torch.split(grid, list(nums))
                    lens = [torch.prod(s, dim=1).sum() for s in samples]
                    model_kwargs[key] = torch.cat([samp.repeat(expand_size, *([1]*(samp.dim()-1))) for samp in torch.split(model_kwargs[key], lens)], dim=0)
                elif key in ["image_grid_thw", "video_grid_thw"]:
                    nums = i_nums if key == "image_grid_thw" else v_nums
                    model_kwargs[key] = torch.cat([samp.repeat(expand_size, *([1]*(samp.dim()-1))) for samp in torch.split(model_kwargs[key], list(nums))], dim=0)
        if input_ids is not None: input_ids = input_ids.repeat_interleave(expand_size, dim=0)
        for key in model_kwargs:
            if key not in v_keys and key != "cache_position" and isinstance(model_kwargs[key], torch.Tensor):
                model_kwargs[key] = model_kwargs[key].repeat_interleave(expand_size, dim=0)
        return input_ids, model_kwargs

